package wen.szu.lazyman.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;
import android.view.ViewGroup;

import java.util.List;

/**
 * Created by wen on 2017/10/15.
 */

public class LazyFragmentPagerAdapter extends FragmentPagerAdapter{

    List<Fragment> fragmentList=null;

    public LazyFragmentPagerAdapter(FragmentManager fm, List<Fragment> fragmentList) {

        super(fm);
        this.fragmentList=fragmentList;
    }


    @Override
    public Fragment getItem(int position) {
        return fragmentList.get(position);
    }

    @Override
    public int getCount() {
        return fragmentList.size();
    }

    @Override
    public void destroyItem(ViewGroup container, int position, Object object) {
        //super.destroyItem(container, position, object);
    }
}
